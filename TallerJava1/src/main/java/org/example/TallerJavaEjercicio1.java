package org.example;

public class TallerJavaEjercicio1 {

    public static void main(String[] args) {

//ejercicio 1 muestra los numeros del 1 al 100(ambos incluidos) usando while

        int i = 1;

        while (i <= 100) {
            System.out.println("numero " + i);
            i += 1;

        }

    }
}